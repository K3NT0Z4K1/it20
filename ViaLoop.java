
package vialoop;

public class ViaLoop {

    
    public static void main(String[] args) {
        
        // 3.2 Display Via Loop
        
        int numbers[] = { 1, 2, 3, 4, 5 };
        
        
        for(int i = 0; i < numbers.length; i++){
            
            System.out.println(numbers[i]);
        }




    }
    
}
